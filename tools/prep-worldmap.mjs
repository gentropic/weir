// Pre-project Natural Earth 110m land (public domain) to a compact equirectangular
// SVG path, vendored as vendor/worldmap.js. viewBox is "0 0 360 180" so a point at
// (lon, lat) maps directly: x = lon+180, y = 90-lat — the renderer needs no proj.
// Run once to regenerate: node tools/prep-worldmap.mjs path/to/ne_110m_land.json
import fs from 'node:fs';

const src = process.argv[2] || '/tmp/ne_land.json';
const gj = JSON.parse(fs.readFileSync(src, 'utf8'));
const R = 1;   // decimals (~11 km) — plenty for a thumbnail; compresses well

const r = (n) => Number(n.toFixed(R));
let d = '';
const ring = (coords) => {
  let s = '', px = null, py = null, n = 0;
  for (const [lon, lat] of coords) {
    const x = r(lon + 180), y = r(90 - lat);
    if (x === px && y === py) continue;        // drop consecutive dupes after rounding
    s += (n === 0 ? 'M' : 'L') + x + ' ' + y;
    px = x; py = y; n++;
  }
  if (n >= 3) d += s + 'Z';
};
for (const f of gj.features) {
  const g = f.geometry; if (!g) continue;
  const polys = g.type === 'Polygon' ? [g.coordinates] : g.type === 'MultiPolygon' ? g.coordinates : [];
  for (const poly of polys) for (const rg of poly) ring(rg);
}

const out = `// Generated by tools/prep-worldmap.mjs from Natural Earth 110m land (PUBLIC DOMAIN,
// naturalearthdata.com). Equirectangular: viewBox "0 0 360 180", so (lon,lat) →
// (lon+180, 90-lat). One land path for the offline card mini-map / map view.
export const WORLD_VIEWBOX = '0 0 360 180';
export const WORLD_PATH = ${JSON.stringify(d)};
`;
fs.writeFileSync('vendor/worldmap.js', out);
console.log(`vendor/worldmap.js: ${(out.length / 1024).toFixed(1)} KB (path ${d.length} chars)`);
